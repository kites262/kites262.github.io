/*
题目2 温度转换
	问题描述
	已知华氏温度到摄氏温度的转换公式为：摄氏温度= (华氏温度- 32)×5/9，写程序将给定的华氏温度转换为摄氏温度输出。
	
	输入说明
	只有一个整数，表示输入的华氏温度。
	
	输出说明
	输出一个表示摄氏温度的实数，小数点后保留2位有效数字，多余部分四舍五入。
	
	输入样例
	50
	
	输出样例
	10.00
*/
#include <stdio.h>

int main(){
	int f;
	double c;

	scanf("%d", &f);
	c = 5.0/9.0 * (f - 32.0);
	printf("%.2f", c);

	return 0;
}