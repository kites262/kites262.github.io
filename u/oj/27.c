/*
题目：阶梯电价计费
类别：流程控制	
时间限制：2S
内存限制：10000Kb
问题描述：	
电价分三个档次，[0,110]度电，每度电0.5元；(110,210]度电，超出110部分每度电0.55元，超过210度电，超出210部分每度电0.70元，给出一个家庭一月用电量，请计算出应缴的电费（四舍五入，保留小数点后两位小数）。
*/
// count is DOUBLE!!
#include <stdio.h>

int main(){
    double money = 0;
    double count;
    scanf("%lf", &count);

    if(1){
        money += (count>=110) ? 55 : 0.5*count;
        if(count > 110){
            money += (count<=210) ? 0.55*(count-110) : 55;
            if(count > 210){
                money  += 0.7*(count-210);
            }
        }
    }

    printf("%.2f", money);
}

/*
输入说明：
输入数据为一个正实数，表示一月用电量
输出说明：
输出应缴电费，保留2位小数。
输入样例：
输入样例1
100
输入样例2
200
输入样例3
329
输出样例：
输出样例1
50.00
输出样例2
104.50
输出样例3
193.30
*/