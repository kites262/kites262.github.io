/*
1.	数字字符
问题描述：
通过键盘输入1个整数a（0<=a<=4）,1个数字字符b（’0’<=b<=’5’），求a+b。

输入说明：
整形数据、数字字符通过键盘输入，输入的整形数据介于0和4之间，输入的数字字符介于‘0’和‘5’之间，二个输入数之间用“，”分隔。

输出说明：
分别以整数形式及字符形式输出a+b，输出的二个数之间用“，”分隔。

输入样例：
3 ,5

输出样例：
56，8

*/
#include <stdio.h>

int main(){
    short int a;
    char b;
    scanf("%hd,%c", &a, &b);
    printf("%d,%c",a+b,a+b);
}