/*
题目描述：
   编写程序，根据某城市普通出租车收费标准进行车费计算。标准如下：
   起步里程为3公里，起步费10元；
   超出起步里程后10公里内，每公理2元；
   超过10公里以上的部分加收50%的回空补贴费，即每公里3元；
   营运过程中，因路阻及乘客要求临时停车等待的，按每5分钟2元计费(不足5分钟不收费)。

输入格式：共一行，包括行驶里程（单位为公里，精确到小数点后1位）与等待时间（整数，单位为分钟），其间以空格分隔

输出格式：在一行中输出乘客应支付的车费（单位为元），结果四舍五入保留整数。

示例：
输入：40.0 7
输出: 116
*/
#include <stdio.h>

int main(){
    double a, b, e=10;
    scanf("%lf%lf", &a, &b);
    if(a>3){
        e += (a>=10) ? 14 : 2*(a-3);
        if(a>10){
        e += 3*(a-10);
        }
    }
    if(b>=5){
        e += 2*(int)(b/5);
    }
    
    printf("%.0lf", e);

}